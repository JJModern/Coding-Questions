import collections


def shortestPath(grid, k):
    """
    :type grid: List[List[int]]
    :type k: int
    :rtype: int
    """
    m = len(grid)
    n = len(grid[0])

    # 0 empty, 1 obstacle (x, y, step, kleft)
    curr_x, curr_y, curr_step, curr_k = (0, 0, 0, k)
    queue = collections.deque([(curr_x, curr_y, curr_step, curr_k)])

    while queue:
        curr_x, curr_y, curr_step, curr_k = queue.popleft()
        if (curr_x, curr_y) == (m - 1, n - 1):
            return curr_step

        if curr_x + 1 <= m - 1:
            if grid[curr_x + 1][curr_y] == 1:
                if curr_k >= 1:
                    queue.append((curr_x + 1, curr_y, curr_step + 1, curr_k - 1))
            else:
                queue.append((curr_x + 1, curr_y, curr_step + 1, curr_k))

        if curr_y + 1 <= n - 1:
            if grid[curr_x][curr_y + 1] == 1:
                if curr_k >= 1:
                    queue.append((curr_x, curr_y + 1, curr_step + 1, curr_k - 1))
            else:
                queue.append((curr_x, curr_y + 1, curr_step + 1, curr_k))

    print(curr_x, curr_y, curr_step, curr_k)
    return -1


